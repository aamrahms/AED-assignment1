    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        int selectedRow =tblDeliveries.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please pick an order for delivery!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            
            Order order= orders.get(selectedRow);
            if(order.getStatus().equals("Ready"))
            {
                AssignDeliveryJPanel deliveryRequest= new AssignDeliveryJPanel(userProcessContainer,order,ecosystem,userAccount);
                userProcessContainer.add("AssignDeliveryMan",deliveryRequest);
                CardLayout cardlayout= (CardLayout) userProcessContainer.getLayout();
                cardlayout.next(userProcessContainer);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Order is not Ready yet, Check once order is ready!", "Warning", JOptionPane.WARNING_MESSAGE);
       
            }
            

        }

    }//GEN-LAST:event_btnAssignActionPerformed

