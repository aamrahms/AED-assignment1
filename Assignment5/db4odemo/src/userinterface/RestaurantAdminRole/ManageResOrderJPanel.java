    private void btnSetstatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetstatusActionPerformed
        // TODO add your handling code here:
        int selectedRow =tblDeliveries.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please pick an order for delivery!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Order order= orders.get(selectedRow);
            order.setStatus("Ready");
            populateTable();
        }
    }//GEN-LAST:event_btnSetstatusActionPerformed

