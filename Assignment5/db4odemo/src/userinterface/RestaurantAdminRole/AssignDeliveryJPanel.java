    public void populateTable(){
        
        md=(DefaultTableModel)tblDeliverymen.getModel();
        md.setRowCount(0);
        Object row[]= new Object[5];
        
        for(DeliveryMan d : deliveryManlist)
        {
            
            row[0]=d.getName();
            row[1]=d.getPhone();
            
            md.addRow(row);
                   
        }
    }

