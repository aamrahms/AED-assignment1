    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        int selectedRow =tblDeliverymen.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please pick an order for delivery!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(order.getStatus().equalsIgnoreCase("Ready"))
            {
                DeliveryMan man= deliveryManlist.get(selectedRow);
                order.setStatus("Assigned");
                man.getDeliveryOrders().add(order);
                JOptionPane.showMessageDialog(this,"Assigned to "+man.getName());
            }
            else{
                JOptionPane.showMessageDialog(null, "Order is already assigned, select another order to deliver", "Warning", JOptionPane.WARNING_MESSAGE);
       
            }
            
            
        }
    }//GEN-LAST:event_btnAssignActionPerformed

